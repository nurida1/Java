package day02_Variables;

import java.sql.SQLOutput;

public class EscapeSequences {
    public static void main (String[] args) {
        System.out.println("Hello");
        System.out.println("\tHello");
        System.out.println("\t\t\t\t\t\tHello");
        //\tmeans a paragraph space

        System.out.println("Cybertek School");
        System.out.println("Cybertek \nSchool");
        System.out.println();
        System.out.println("\n\nMy\nName\nIs\nNurida");
        //\n means new line

        //print: My favourite TV show is "Game of Thrones"
        System.out.println("My Favourite TV show is \"Game of Thrones\"");
        // \ means double quote, prints double quote to the console

        System.out.println("/");
        System.out.println("My favourite book is \'Java'"); // \' prints the'
        System.out.println("My favourite book is \'Java'");






    }



}
